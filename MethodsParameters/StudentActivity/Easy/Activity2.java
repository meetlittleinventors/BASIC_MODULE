class Activity2 {
    static int doubleValue(int x) {
        return x * 2;
    }

    public static void main(String[] args) {
        System.out.println("double(5) = " + doubleValue(5));
        System.out.println("double(10) = " + doubleValue(10));
        System.out.println("double(15) = " + doubleValue(15));
    }
}
