public class PrintVariables {
    public static void main(String[] args) {
        int age = 18;
        double marks = 92.5;
        char grade = 'A';
        boolean isPass = true;

        System.out.println("Age: " + age);
        System.out.println("Marks: " + marks);
        System.out.println("Grade: " + grade);
        System.out.println("Passed? " + isPass);
    }
}
